package model;

/**
 *
 * @author edisucipto
 */
public class ModelPakan {
    
    private String idPakan;
    private String namaPakan;
    private String jenisPakan;
    private double beratKering;
    private double beratSegar;
    private double proteinKasar;
    private double energi;
    

    public String getIdPakan() {
        return idPakan;
    }

    public void setIdPakan(String idPakan) {
        this.idPakan = idPakan;
    }

    public String getNamaPakan() {
        return namaPakan;
    }

    public void setNamaPakan(String namaPakan) {
        this.namaPakan = namaPakan;
    }

    public String getJenisPakan() {
        return jenisPakan;
    }

    public void setJenisPakan(String jenisPakan) {
        this.jenisPakan = jenisPakan;
    }

    public double getBeratKering() {
        return beratKering;
    }

    public void setBeratKering(double beratKering) {
        this.beratKering = beratKering;
    }

    public double getProteinKasar() {
        return proteinKasar;
    }

    public void setProteinKasar(double proteinKasar) {
        this.proteinKasar = proteinKasar;
    }

    public double getEnergi() {
        return energi;
    }

    public void setEnergi(double energi) {
        this.energi = energi;
    }

    public double getBeratSegar() {
        return beratSegar;
    }

    public void setBeratSegar(double beratSegar) {
        this.beratSegar = beratSegar;
    }

      
}
